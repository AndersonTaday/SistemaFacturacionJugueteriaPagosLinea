    private void mostrarDatos() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nro");
        modelo.addColumn("Juguete");
        modelo.addColumn("Descripción");
        modelo.addColumn("Precio Unitario");
        modelo.addColumn("Precio Total");
        JTReporteCompra.setModel(modelo);
        String[] datos = new String[5];
        try {
            Statement stAux = cn.createStatement();
            Statement st = cn.createStatement();
            ResultSet rsAux = null;
            ResultSet rs = null;

            if (cbxTipoBusqueda.getSelectedIndex() == 0) {

                rsAux = stAux.executeQuery("SELECT idJuguete FROM juguete WHERE codigo = " + tfTipoBusqueda.getText());
                rsAux.next();

                rs = st.executeQuery("SELECT idDetCompra,detacjug_idJuguete,descripcion,precioUnitario,precioTotal FROM detallecompra WHERE detacjug_idJuguete = " + rsAux.getInt(1));
            } else if (cbxTipoBusqueda.getSelectedIndex() == 1) {
                rsAux = stAux.executeQuery("SELECT idCompra FROM compra WHERE numCompra = " + tfTipoBusqueda.getText());
                rsAux.next();

                rs = st.executeQuery("SELECT idDetCompra,detacjug_idJuguete,descripcion,precioUnitario,precioTotal FROM detallecompra WHERE compras_idCompra = " + rsAux.getInt(1));
            }
            while (rs.next()) {
                Statement stTmp = cn.createStatement();
                ResultSet rsTmp = stTmp.executeQuery("SELECT nombre FROM juguete WHERE idJuguete = " + rs.getInt(2));

                rsTmp.next();
                datos[0] = rs.getString(1);
                datos[1] = rsTmp.getString(1);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                modelo.addRow(datos);
            }
            JTReporteCompra.setModel(modelo);

        } catch (SQLException e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "Error" + " " + e.getMessage(), "mensaje", JOptionPane.ERROR_MESSAGE);
        }

    }


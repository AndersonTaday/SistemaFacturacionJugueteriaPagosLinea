    private void BuscarCliente(String ced) {
        try {
            List<Persona> lis = null;
            lis = personadb.buscarPersona(ced, lis);
            if (lis.size() > 0) {
                for (Persona per : lis) {
                    txtCedula.setText(per.getCI());
                    txtNombre.setText(per.getNombre());
                    txtApellido.setText(per.getApellido());
                    txtDireccion.setText(per.getDireccion());
                    txtTelefono.setText(per.getTelefono());
                    txtCorreoElectronico.setText(per.getCorreoElectronico());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Cliente no encontrado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                txtCedula.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al buscar" + e.getMessage(), "mensaje", JOptionPane.ERROR_MESSAGE);
        }

    }


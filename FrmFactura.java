    private void CargarTabla(Vector lista) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Marca");
        modelo.addColumn("Modelo");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio Uitario");
        modelo.addColumn("Precio Total");
        Object[] info = new Object[6];
        Controlador_Factura obj;
        double sum = 0;
        for (int i = 0; i < lista.size(); i++) {
            obj = (Controlador_Factura) lista.elementAt(i);
            info[0] = obj.getNombre();
            info[1] = obj.getMarca();
            info[2] = obj.getModelo();
            info[3] = obj.getCantidad();
            info[4] = obj.getPrecioU();
            info[5] = obj.Mult();
            modelo.addRow(info);
            sum += obj.Mult();
        }

        JTFactura.setModel(modelo);
        txtSubtotal.setText(String.valueOf(redondear(sum - (sum * 0.12))));
        txtIva.setText(String.valueOf(redondear(sum * 0.12)));
        txtTotal.setText(String.valueOf(redondear(sum)));
    }

    private double redondear(double num) {
        return Math.rint(num * 100) / 100;
    }

    private void BuscarCliente(String ced) {
        try {
            List<Persona> lis = null;
            lis = personadb.buscarPersona(ced, lis);
            if (lis.size() > 0) {
                for (Persona per : lis) {
                    persona = per;
                    txtCedula.setText(per.getCI());
                    txtNombre.setText(per.getNombre());
                    txtApellido.setText(per.getApellido());
                    txtDireccion.setText(per.getDireccion());
                    txtTelefono.setText(per.getTelefono());
                    txtCorreoElectronico.setText(per.getCorreoElectronico());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Cliente no encontrado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                txtCedula.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al buscar" + e.getMessage(), "mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void BuscarJuguete(String cod) {
        try {
            List<Juguete> lis = null;
            lis = juguete.buscarJuguete(cod, lis);
            if (lis.size() > 0) {
                for (Juguete per : lis) {
                    txtCodigoJuguete.setText(per.getCodigo());
                    txtNombreJuguete.setText(per.getNombre());
                    txtMarca.setText(per.getMarca());
                    txtModelo.setText(per.getModelo());
                    txtPrecioJuguete.setText(per.getPrecio());
                }
            } else {
                JOptionPane.showMessageDialog(null, "Juguete no encontrado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                txtCedula.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al buscar" + e.getMessage(), "mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }


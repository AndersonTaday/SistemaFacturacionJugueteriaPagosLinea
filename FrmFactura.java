    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        SimpleDateFormat fecha = new SimpleDateFormat("dd/MM/yyyy");
        String fechaCadena = fecha.format(new Date());

        if (ValidarCampos() == true) {
            try {
                PreparedStatement pps = (PreparedStatement) cn.prepareStatement("INSERT INTO factura(CI,correoElectronico,direccion,fecha,nombreCliente,telefono) VALUES(?,?,?,?,?,?)");
                pps.setString(1, txtCedula.getText());
                pps.setString(2, txtCorreoElectronico.getText());
                pps.setString(3, txtDireccion.getText());
                pps.setString(4, fechaCadena);
                pps.setString(5, txtNombre.getText());
                pps.setString(6, txtTelefono.getText());
                pps.executeUpdate();
                Statement st = cn.createStatement();
                ResultSet rs = st.executeQuery("SELECT MAX(idFactura) FROM factura");
                rs.next();
                int id = rs.getInt(1);
                FacturaImpresa fi = new FacturaImpresa(id, fechaCadena, (DefaultTableModel) JTFactura.getModel(), persona, txtSubtotal.getText(), txtIva.getText(), txtTotal.getText());
                fi.setLocationRelativeTo(null);
                fi.setVisible(true);
                dispose();
            } catch (SQLException ex) {
                System.out.println(ex);
                Logger.getLogger(FrmJuguete.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Llenar campos en blanco", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnPagarActionPerformed


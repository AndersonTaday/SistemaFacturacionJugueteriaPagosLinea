    private void mostrarDatos() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Cédula");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Usuario");
        modelo.addColumn("Contraseña");
        modelo.addColumn("Rol");
        modelo.addColumn("Estado");
        JTUsuario.setModel(modelo);
        String[] datos = new String[7];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT persona_idPersona,clave,estadoCuenta,usuario FROM cuenta");
            while (rs.next()) {
                Statement sp = cn.createStatement();
                ResultSet rsPer = sp.executeQuery("SELECT CI,nombre,apellido,rol_idRol FROM persona WHERE idPersona = " + rs.getInt(1));
                rsPer.next();
                Statement sc = cn.createStatement();
                ResultSet rsRol = sc.executeQuery("SELECT tipo FROM rol WHERE idRol = " + rsPer.getInt(4));
                rsRol.next();
                datos[0] = rsPer.getString(1);
                datos[1] = rsPer.getString(2);
                datos[2] = rsPer.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(2);
                datos[5] = rsRol.getString(1);
                datos[6] = rs.getString(3);

                modelo.addRow(datos);
            }
            JTUsuario.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error" + " " + e.getMessage(), "mensaje", JOptionPane.ERROR_MESSAGE);
        }

    }


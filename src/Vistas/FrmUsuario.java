    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT MAX(idCuenta) FROM cuenta");
            rs.next();
            int idCuenta = rs.getInt(1) + 1;
            PreparedStatement pps = (PreparedStatement) cn.prepareStatement("INSERT INTO cuenta(idCuenta,clave,estadoCuenta,usuario,persona_idPersona) VALUES(?,?,?,?,?)");
            pps.setInt(1, idCuenta);
            pps.setString(2, txtClave.getText());
            pps.setString(3, ("A"));
            pps.setString(4, txtUsuario.getText());
            pps.setInt(5, id);
            pps.executeUpdate();
            String insertar = "UPDATE persona SET rol_idRol = ? WHERE idPersona =" + id;
            PreparedStatement pstmt = (PreparedStatement) cn.prepareStatement(insertar);
            pstmt.setInt(1, cbxRol.getSelectedIndex() + 1);
            pstmt.executeUpdate();
        } catch (SQLException e) {
            System.out.println(e);
        }
        mostrarDatos();


    }//GEN-LAST:event_btnGuardarActionPerformed

